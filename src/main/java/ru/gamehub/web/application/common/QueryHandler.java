package ru.gamehub.web.application.common;

/**
 * Унифицированный интерфейс для обработки запросов приложения в стиле DDD/CQRS.
 * <p>
 * Реализуется каждым сервисом, отвечающим за выполнение конкретного действия (use case),
 * принимающим запрос типа {@code C} и возвращающим результат типа {@code R}.
 *
 * @param <C> тип запроса, расширяющий {@link Query}
 * @param <R> тип результата обработки запроса
 */
public interface QueryHandler<C extends Query, R>{
    /**
     * Обрабатывает переданный запрос.
     *
     * @param command команда, содержащая входные данные
     * @return результат выполнения запроса
     */
    R handle(C command);
}
